      {
        $("#minatTxt").prop('disabled',false);
      }
      else{
        $("#minatTxt").prop('disabled',true);
      }
    });

    $('#pilMinat').change(function() {
        if ($(this).val() === 'Non-Akademik') {
          $('#Aka').hide();
          $( "#Non" ).fadeIn( "slow", function() {
            $(this).show();
          });
        } else if($(this).val() === 'Akademik'){
          $('#Non').hide();
          $( "#Aka" ).fadeIn( "slow", function() {
            $(this).show();
          });
        }
    });
  });

  function klikLain(){
    $(".lain2").prop('required',true);
    $( ".lain" ).fadeIn( "slow", function() {
      $(this).show();
    });
  }

